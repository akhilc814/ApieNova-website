// Current language
let currentLang = localStorage.getItem('apienova_lang') || 'en';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('apienova_lang', lang);

  const html = document.documentElement;
  const rtlStyle = document.getElementById('rtl-style');

  if (lang === 'ar') {
    html.setAttribute('dir', 'rtl');
    html.setAttribute('lang', 'ar');
    rtlStyle.removeAttribute('disabled');
  } else {
    html.setAttribute('dir', 'ltr');
    html.setAttribute('lang', lang);
    rtlStyle.setAttribute('disabled', '');
  }

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = TRANSLATIONS[lang]?.[key];
    if (val !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else if (el.tagName === 'OPTION') {
        el.textContent = val;
      } else {
        el.innerHTML = val;
      }
    }
  });

  // Update active lang button
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
  const activeBtn = document.getElementById('lang-' + lang);
  if (activeBtn) activeBtn.classList.add('active');

  // Update page lang attribute
  document.title = lang === 'ru'
    ? 'ApieNOVA — Роскошный Консьерж · Лазурный Берег'
    : lang === 'ar'
    ? 'ApieNOVA — كونسيرج فاخر · كوت دازور'
    : 'ApieNOVA — Luxury Concierge · Côte d\'Azur';
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
  setLang(currentLang);
});
